		Per diagnostica per immagini -o imaging- si intendono le modalità di formazione delle immagini biomediche utilizzate a scopo diagnostico. Tra queste l’ecografia, la risonanza magnetica (RMN) e la Tomografia assiale computerizzata (TAC). Presso la Clinica San Martino l’esame ecografico è eseguito sia per l’acquisizione di immagini che a  completamento dell’attività medica, nell’ambulatorio di cardiologia inoltre il test cardiovascolare da sforzo con cicloergometro (Cyclette) consente di valutare l’organismo in condizioni di stress. La diagnostica è donna con gli esami mammografici che consentono una pronta interventistica senologica, ovvero la biopsia delle lesioni mammarie che può essere appunto effettuata con indagini strumentali quali l’ecografia e la mammografia. Le visite specialistiche sono eseguite con tecnica digitale diretta che consente di ottenere immagini di elevata qualità, utilizzando dosi di irradiazione sensibilmente basse. La prevenzione è completata anche da apparecchi ecografici di ultima generazione. La diagnostica inoltre da un lato è accessoria e complementare al reparto chirurgico, dall’altro risponde al crescente bisogno di salute per l’analisi delle patologie del distretto testa-collo.
