		<div class="section_title"> Tomografia Computerizzata (TAC) </div>
		 Metodica di diagnostica per immagini, che sfruttando radiazioni ionizzanti (raggi X) consente di riprodurre sezioni o strati corporei del paziente ed effettuare elaborazioni tridimensionali. Il nuovo reparto è dotato di TAC Siemens Definition AS+  a scansione volumetrica di 128 strati con rotazione in grado di acquisire 180 cm in soli 10 secondi. Tecnologia che permette acquisizione di immagini ad altissima risoluzione, ma con dosaggi di radiazioni estremamente bassi.
	</div>
