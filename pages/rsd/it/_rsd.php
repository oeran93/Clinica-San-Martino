		<div class="section_title"> Radiografia Convenzionale </div>
			 La tecnica si basa sull’interazione tra un fascio di fotoni (raggi X) diretti da una sorgente a un recettore e la materia interposta, un corpo biologico. Gli atomi di tale corpo interferente impediscono al fotone di raggiungere il recettore, che quindi riprodurrà un’immagine fedele del corpo “in negativo”. Il nuovo reparto è dotato di sistema radiologico digitale telecomandato Siemens Luminos RF Classic che, grazie alla tecnologia digitale, permette di acquisire immagini radiografiche di tutto il corpo con un notevole risparmio nella dose di radiazioni.
