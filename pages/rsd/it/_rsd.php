		<div class="section_title"> Risonanza Magnetica </div>
		tecnica diagnostica che fornisce immagini dettagliate del corpo umano utilizzando campi magnetici e onde radio, senza esporre il paziente a nessun tipo di radiazioni ionizzanti. Il nuovo reparto è equipaggiato con di risonanza magnetica Siemens Magnetom Essenza da 1,5 Tesla - apparecchiatura ultra soft di soli 145 cm con magnete molto leggero – dotata di 25 elementi integrati di bobina con esclusiva  Tecnologia Tim (Total Imaging Matrix), per esami rapidi e di altissima qualità. Tale apparecchiatura, estremamente confortevole, si presta anche a pazienti ansiosi o claustrofobici.
