		<div class="section_title"> TAC volumetrica Cone Beam 3D </div>
		 Nuova tecnologia di tomografia computerizzata (TAC) dedicata allo studio del massiccio facciale. Utilizzata principalmente in campo implantologico e della chirurgia orale e maxillofacciale, grazie alla tecnologia 3D, consente l’acquisizione di immagini complete e ricche di informazioni permettendo così la formulazione di diagnosi precise.
		 Grazie all’acquisizione volumetrica (Cone Beam) utilizzando un fascio di raggi x conico si realizza una drastica riduzione della dose di radiazioni  cui si espongono i pazienti (circa 30-60 volte inferiore ad una convenzionale TAC).
